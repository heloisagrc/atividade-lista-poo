class Hora {
    constructor(hora) {
      this.hora = hora;
    }
  
    getHora() {
      return this.hora;
    }
  
    setHora(hora) {
      this.hora = hora;
    }
  }
  class DataHora {
    constructor(data, hora) {
      this.data = data;
      this.hora = hora;
    }
  
    getData() {
      return this.data;
    }
  
    setData(data) {
      this.data = data;
    }
  
    getHora() {
      return this.hora;
    }
  
    setHora(hora) {
      this.hora = hora;
    }
  }